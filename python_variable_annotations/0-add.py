#!/usr/bin/env python3
"""
type-annotated that takes a float a and b and returns their sum as a float.

"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns their sum.

    Args:
        a (float): The first num.
        b (float): The second num.

    Returns:
        float: The sum of the two num.
    """
    return a + b
